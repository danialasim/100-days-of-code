# Day 2 - Understanding Python Data Types
# This file demonstrates different data types in Python and their usage

#--------------------
# String Data Type
#--------------------

# String subscripting: accessing individual characters
# String indices start at 0, so [0] gets the first character
print("Hello"[0])  # Output: H

# String concatenation with other strings
print("123" + "345")  # Output: 123345 (as a string)

#--------------------
# Integer Data Type
#--------------------

# Integer operations
print(123 + 345)  # Output: 468 (as a number)

# Large numbers can use underscores for readability
# The underscores are ignored by Python
num = 123_456_789  # Same as 123456789

#--------------------
# Float Data Type
#--------------------

# Float (decimal) numbers
pi = 3.14159  # Storing a decimal number

# Boolean Data Type
is_true = True  # Boolean values are capitalized
is_false = False