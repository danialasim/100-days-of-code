# This file demonstrates variable assignment and manipulation

# Variable assignment with a string literal
name = "Jack"
print(name)

# Variable assignment with user input
name = input("What is your name?")  # Overwrites the previous value of 'name'
print(name)

# Using the len() function to count characters in a string
new_name = input("Whats is your name?")
length = len(new_name)  # Stores the length of the input string
print(length)  # Prints the number of characters in the name