# Demonstration of List and Tuple Slicing in Python

# Example collections
piano_keys = ["a", "b", "c", "d", "e", "f", "g"]  # List example
piano_tuple = ("do", "re", "mi", "fa", "so", "la", "ti")  # Tuple example

# Slicing examples
print(piano_tuple[1:])  # Slice from index 1 to end
# Other common slicing patterns:
# piano_keys[2:5]  # Elements from index 2 to 4
# piano_keys[:3]   # Elements from start to index 2
# piano_keys[::2]  # Every second element
# piano_keys[::-1] # Reverse the sequence